def _bars_context(symbol: str, bars: pd.DataFrame) -> str:
    stats = compute_bar_stats(bars)
    if not stats:
        return f"Insufficient bar data for {symbol}."
    return (
        f"Market context ({symbol}, last {stats['n_days']} trading days):\n"
        f"- Last close: ${stats['last_close']:.2f}\n"
        f"- {stats['lookback_20']}-day price change: {stats['pct_20d']:+.1f}%\n"
        f"- {stats['lookback_10']}-day annualized volatility: {stats['vol_10d_annualized']:.1f}%"
    )


def _parse_response(text: str) -> dict:
    """Parse LLM response, stripping markdown fences if present."""
    text = text.strip()
    text = re.sub(r"^```(?:json)?\s*", "", text)
    text = re.sub(r"\s*```$", "", text)
    return json.loads(text.strip())


def apply_claude_overlay(
    signal: Signal,
    bars: pd.DataFrame,
    groq_key: str | None,
    groq_model: str,
    claude_key: str | None,
    claude_model: str,
    gemini_key: str | None = None,
    gemini_model: str = "gemini-3.1-flash-lite",
    config=None,  # optional, enables Finnhub news path
    sentiment_client=None,  # NEW — crypto sentiment overlay
    repo=None,  # optional PortfolioRepository — enables trade-memory context
    strategy_name: str | None = None,
    regime: str | None = None,
) -> Signal:
    """Call LLM to review a quant signal. Returns original signal on any failure."""
    try:
        cache_key = (signal.symbol, signal.side)
        now = time.monotonic()
        if repo is not None:
            try:
                cached_row = repo.get_overlay_cache(signal.symbol, signal.side, _OVERLAY_TTL)
            except Exception:
                logger.warning("overlay cache read failed for %s, continuing without it", signal.symbol, exc_info=True)
                cached_row = None
            if cached_row is not None:
                logger.debug("overlay cache hit (db) symbol=%s side=%s", signal.symbol, signal.side)
                _log_llm_call(repo, "cache", signal.symbol, cache_hit=True, usage=None)
                return Signal(
                    symbol=signal.symbol,
                    side=cached_row["side"],
                    strength=cached_row["strength"],
                    reason=cached_row["reason"],
                )
        elif cache_key in _OVERLAY_CACHE:
            ts, cached = _OVERLAY_CACHE[cache_key]
            if now - ts < _OVERLAY_TTL:
                logger.debug("overlay cache hit symbol=%s side=%s age=%.0fs", signal.symbol, signal.side, now - ts)
                _log_llm_call(repo, "cache", signal.symbol, cache_hit=True, usage=None)
                return cached

        is_crypto = "/" in signal.symbol
        system_prompt = _CRYPTO_SYSTEM_PROMPT if is_crypto else _EQUITY_SYSTEM_PROMPT

        # Use Finnhub news if available, else Alpaca news
        from trader.overlay.news_context import fetch_news_with_fallback
        news = fetch_news_with_fallback(signal.symbol, config) if config else fetch_news(signal.symbol)
        news_section = news if news else "No recent news available."

        sentiment_str = ""
        if is_crypto and sentiment_client is not None:
            snap = sentiment_client.get_sentiment(signal.symbol)
            sentiment_str = sentiment_client.format_for_overlay(snap)

        user_message = (
            f"Signal: {signal.side} {signal.symbol} | "
            f"strength={signal.strength:.2f} | reason: {signal.reason}\n\n"
            f"{_bars_context(signal.symbol, bars)}\n\n"
            f"{news_section}"
        )
        if sentiment_str:
            user_message += f"\n\n{sentiment_str}"

        memory_shadow = bool(config and getattr(config.risk, "trade_memory_shadow", False))
        memory_live = bool(config and getattr(config.risk, "trade_memory_live", False))
        if repo is not None and (memory_shadow or memory_live):
            try:
                outcomes = repo.get_recent_outcomes(symbol=signal.symbol, limit=3)
                if outcomes:
                    lines = [f"Recent closed trades on {signal.symbol}:"]
                    for o in outcomes:
                        lines.append(
                            f"- {o['closed_at'][:10]} {o['strategy']}: {o['pnl_pct']:+.1%} "
                            f"({o['exit_reason']}). Entry rationale was: "
                            f"{o.get('entry_overlay_rationale') or 'n/a'}"
                        )
                    memory_section = "\n".join(lines)
                    if memory_live:
                        user_message += f"\n\n{memory_section}"
                    else:
                        logger.info("trade_memory shadow symbol=%s would inject: %s", signal.symbol, memory_section)
            except Exception:
                logger.warning("trade memory lookup failed for %s, continuing without it", signal.symbol)

        logger.info(
            "overlay request symbol=%s side=%s strength=%.2f news_lines=%d",
            signal.symbol, signal.side, signal.strength,
            news_section.count("\n") + 1 if news else 0,
        )
        logger.debug("overlay user_message:\n%s", user_message)

        raw, usage = call_llm(system_prompt, user_message, 256, groq_key, groq_model, claude_key, claude_model, gemini_key, gemini_model)
        if not raw:
            logger.warning("overlay: no LLM response for %s, passing through", signal.symbol)
            return signal
        _log_llm_call(repo, usage.provider if usage else "unknown", signal.symbol, cache_hit=False, usage=usage)

        parsed = _parse_response(raw)
        logger.info(
            "overlay response symbol=%s action=%s strength=%s rationale=%r",
            signal.symbol, parsed.get("action"), parsed.get("strength"), parsed.get("rationale", ""),
        )

        action = parsed.get("action")
        rationale = str(parsed.get("rationale", ""))

        if action not in {"approve", "veto"}:
            logger.warning("overlay: invalid action %r for %s, approving", action, signal.symbol)
            action = "approve"

        raw_strength = parsed.get("strength")
        try:
            strength = float(raw_strength)
            if not (0.0 <= strength <= 1.0):
                raise ValueError
        except (TypeError, ValueError):
            logger.warning("overlay: bad strength %r for %s, using original", raw_strength, signal.symbol)
            strength = signal.strength

        if action == "veto":
            result = Signal(
                symbol=signal.symbol,
                side="hold",
                strength=0.0,
                reason=f"[overlay veto] {rationale}",
            )
        else:
            result = Signal(
                symbol=signal.symbol,
                side=signal.side,
                strength=float(strength),
                reason=f"[overlay approved] {rationale}",
            )

        if repo is not None:
            try:
                repo.set_overlay_cache(signal.symbol, signal.side, result.side, result.strength, result.reason)
            except Exception:
                logger.warning("overlay cache write failed for %s", signal.symbol, exc_info=True)
        else:
            _OVERLAY_CACHE[cache_key] = (now, result)
        return result

    except Exception as exc:  # noqa: BLE001
        logger.warning("overlay failed for %s, passing through: %s", signal.symbol, exc)
        return signal
