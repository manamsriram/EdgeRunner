"""LLM overlay — Phase 5.

Non-load-bearing: any failure returns the original signal unchanged.
LLM may veto (side→"hold", strength=0.0) or adjust strength ∈ [0.0, 1.0].
It never originates a trade, flips buy↔sell, or sets position size.
"""
from __future__ import annotations

import json
import logging
import re
import time

import pandas as pd

from trader.overlay.cost_tracking import estimate_cost_usd
from trader.overlay.llm_client import call_llm
from trader.overlay.market_stats import compute_bar_stats
from trader.overlay.news_context import fetch_news
from trader.strategy.base import Signal

logger = logging.getLogger(__name__)

# ---- In-process overlay cache ----

# (symbol, side) -> (monotonic_timestamp, Signal)
_OVERLAY_CACHE: dict[tuple[str, str], tuple[float, Signal]] = {}
_OVERLAY_TTL = 1800.0  # 30 minutes


def _clear_overlay_cache() -> None:
    """Test helper — clears the in-process overlay cache."""
    _OVERLAY_CACHE.clear()


def _log_llm_call(repo, provider: str, symbol: str, cache_hit: bool, usage) -> None:
    """Best-effort cost-log write. Never raises — logging must not affect the overlay."""
    if repo is None:
        return
    try:
        repo.record_llm_call(
            provider=provider,
            call_site="overlay",
            symbol=symbol,
            cache_hit=cache_hit,
            input_tokens=usage.input_tokens if usage else 0,
            output_tokens=usage.output_tokens if usage else 0,
            est_cost_usd=estimate_cost_usd(usage),
        )
    except Exception:
        logger.warning("llm call-log write failed for %s", symbol, exc_info=True)


